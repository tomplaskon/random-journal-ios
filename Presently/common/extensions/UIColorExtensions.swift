import UIKit

extension UIColor {
    static var rjGray: UIColor {
        return UIColor(red: 62/255, green: 64/255, blue: 64/255, alpha: 1.0)
    }
    
    static var rjGreen: UIColor {
        return UIColor(red: 176/255, green: 248/255, blue: 118/255, alpha: 1.0)
    }
    
    static var rjDarkGray: UIColor {
        return UIColor(red: 74/255, green: 74/255, blue: 74/255, alpha: 1.0)
    }
}
